import UIKit

extension UIColor {
    
    class var navyBlue: UIColor {
        return UIColor(red: 32 / 255, green: 30 / 255, blue: 67 / 255, alpha: 1)
    }
    
    class var cyanBlue: UIColor {
        return UIColor(red: 19 / 255, green: 75 / 255, blue: 112 / 255, alpha: 1)
    }
    
    class var teal: UIColor {
        return UIColor(red: 80 / 255, green: 140 / 255, blue: 155 / 255, alpha: 1)
    }
    
    class var gray238: UIColor {
        return UIColor(red: 238 / 255, green: 238 / 255, blue: 238 / 255, alpha: 1)
    }
}
